#include "ft_printf.h"

int	ft_putnbr(int n)
{
	int	res;

	res = 0;
	if (n == -2147483648)
		return (write(1, "-2147483648", 11));
	if (n < 0)
	{
		res += ft_putchar('-');
		n *= -1;
	}
	if (n >= 10)
		res += ft_putnbr(n / 10);
	res += ft_putchar(n % 10 + '0');
	return (res);
}
